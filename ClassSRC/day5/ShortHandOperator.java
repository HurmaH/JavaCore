package day5;

public class ShortHandOperator {
	
	public static void main(String[] args) {
		//shorthand --> short cut
		
		int i = 10;
		
		System.out.println("the score is " +i);
		
		i +=5; //i=15
		System.out.println("the score is " +i);
		
		i-=2; //i=13;
		System.out.println("the score is " +i);
		
		i*=10;
		System.out.println("the score is " +i);
		i/=5;
		System.out.println("the score is " +i);
		i%=7;
		System.out.println("the score is " +i);
	}

}
