package day5;

public class Incr_Decr {
	
	public static void main(String[] args) {
		
		int i = 10;
		System.out.println(1+2);
		
		i = i+1;
		System.out.println("the score is "+i);
		
		i+=1;
		System.out.println("the score is "+i);
		
		i++;
		System.out.println("the score is "+i);
		
		++i;
		System.out.println("the score is "+i);
	}

}
