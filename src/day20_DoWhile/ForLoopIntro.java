package day20_DoWhile;

public class ForLoopIntro {
	
	public static void main(String[] args) {
		
		for (int i=0; i<5; i++) {
			System.out.println("Be Happy!" +i);
		}
		
		//count from 1-10
		for (int i=1; i<=10; i++) {
			System.out.print(i+" ");
		}
		
		System.out.println();
		//count from 10-1
		for (int i=10; i>=1; i--) {
			System.out.print(i+" ");
		}
	}

}
